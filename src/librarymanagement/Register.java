    private void tfIdentityNumKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfIdentityNumKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_TAB) {
            if (this.tfIdentityNum.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Please insert Identity Number!", "Message", 2);
                tfIdentityNum.requestFocus();
            } else if (this.tfIdentityNum.getText().length() != 11) {
                JOptionPane.showMessageDialog(null, "Please insert 11 numbers!", "Message", 2);
                tfIdentityNum.requestFocus();
            } else {
                tfPhone.requestFocus();
            }
        } else if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (this.tfIdentityNum.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Please insert Identity Number!", "Message", 2);
                tfIdentityNum.requestFocus();
            } else if (this.tfIdentityNum.getText().length() != 11) {
                JOptionPane.showMessageDialog(null, "Please insert 11 numbers!", "Message", 2);
                tfIdentityNum.requestFocus();
            } else {
                tfPhone.requestFocus();
            }
        }
    }//GEN-LAST:event_tfIdentityNumKeyPressed

