    private void tfPassKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfPassKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_TAB) {
            if ((tfPass.getText().length() <= 7)) {
                JOptionPane.showMessageDialog(this, "Please enter at least 8 characters.\n", "Message", JOptionPane.ERROR_MESSAGE);
                tfPass.requestFocus();
            } else {
                tfRePass.requestFocus();
            }
        } else if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if ((tfPass.getText().length() <= 7)) {
                JOptionPane.showMessageDialog(this, "Please enter at least 8 characters.\n", "Message", JOptionPane.ERROR_MESSAGE);
                tfPass.requestFocus();
            } else {
                tfRePass.requestFocus();
            }
        } else {
            tfPass.requestFocus();
        }
    }//GEN-LAST:event_tfPassKeyPressed

    private void tfRePassKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfRePassKeyPressed
        // TODO add your handling code here:
        String pass = tfPass.getText();
        String repass = tfRePass.getText();

        if (evt.getKeyCode() == KeyEvent.VK_TAB) {
            if (!pass.equals(repass)) {
                JOptionPane.showMessageDialog(this, "Password do not match. Please try again.\n", "Message", JOptionPane.ERROR_MESSAGE);
                tfRePass.requestFocus();
            } else {
                tfName.requestFocus();
            }
        } else if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!pass.equals(repass)) {
                JOptionPane.showMessageDialog(this, "Password do not match. Please try again.\n", "Message", JOptionPane.ERROR_MESSAGE);
                tfRePass.requestFocus();
            } else {
                tfName.requestFocus();
            }
        } else {
            tfRePass.requestFocus();
        }
    }//GEN-LAST:event_tfRePassKeyPressed

    private void tfNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfNameKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_TAB) {
            if (tfName.getText() == null) {
                tfName.requestFocus();
            } else {
                tfBirthday.requestFocus();
            }
        } else if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (tfName.getText() == null) {
                tfName.requestFocus();
            } else {
                tfBirthday.requestFocus();
            }
        }
    }//GEN-LAST:event_tfNameKeyPressed

    private void tfIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfIDKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_TAB) {
            if (this.tfID.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Please insert User ID", "Message", 2);
                tfID.requestFocus();
            } else if (this.tfID.getText().length() != 6) {
                JOptionPane.showMessageDialog(null, "Please insert 6 characters!", "Message", 2);
                tfID.requestFocus();
            } else {
                tfPass.requestFocus();
            }
        } else if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (this.tfID.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Please insert User ID", "Message", 2);
                tfID.requestFocus();
            } else if (this.tfID.getText().length() != 6) {
                JOptionPane.showMessageDialog(null, "Please insert 6 characters!", "Message", 2);
                tfID.requestFocus();
            } else {
                tfPass.requestFocus();
            }
        }
    }//GEN-LAST:event_tfIDKeyPressed

